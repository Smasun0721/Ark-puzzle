package puzzle;

import javax.swing.*;

public class LoginJFrame extends JFrame {
    public LoginJFrame() {
        //设置窗口大小
        this.setSize(500,400);
        //设置窗口标题
        this.setTitle("拼图小游戏 v1.0 登录");
        //设置窗口居中
        this.setLocationRelativeTo(null);
        //设置窗口置顶
        this.setAlwaysOnTop(true);
        //设置窗口关闭
        this.setDefaultCloseOperation(1);
        //设置窗口可见
        this.setVisible(true);
    }
}
